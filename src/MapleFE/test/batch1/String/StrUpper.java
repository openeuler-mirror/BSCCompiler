// Convert a string to all upppercase
public class StrUpper {
  public static void main(String[] args) {
    String x = "Failures are the stepping stone to success.";

    // Convert the given string above to all uppercase
    String upperx = x.toUpperCase();
    
    System.out.println("Original : " + x);
    System.out.println("Uppercase : " + upperx);
  }
}
