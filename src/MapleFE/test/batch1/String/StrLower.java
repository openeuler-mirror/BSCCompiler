// Convert the given string to all lowercase
public class StrLower {
  public static void main(String[] args) {
    String x = "Failures Are The Stepping Stone To Success.";
 
    // Convert the above string to lowercase 
    String lowerx = x.toLowerCase();

    System.out.println("Original : " + x);
    System.out.println("Lowercase : " + lowerx);

  }
}
