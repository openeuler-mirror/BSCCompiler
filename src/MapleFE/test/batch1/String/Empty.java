public class Empty {
  public static void main(String args[]) {
    String x, y, z;
    x = "Hi";
    y = "";
    z = "a";

    System.out.println("Is '" + x + "' Empty?\t" + x.isEmpty());
    System.out.println("Is '" + y + "' Empty?\t" + y.isEmpty());
    System.out.println("Is '" + z + "' Empty?\t" + z.isEmpty());
  }
}
